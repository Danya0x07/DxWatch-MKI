void Test_FontSizes(void)
{
    GFX_SetCursor(0, 0);
    GFX_PrintString("Text");

    GFX_SetupBrush(GfxFontSize_14X10, GfxImageScale_X1, false);
    GFX_SetCursor(0, 1);
    GFX_PrintString("Текст");
}

void Test_LongString(void)
{
    GFX_SetupBrush(GfxFontSize_14X10, GfxImageScale_X1, false);
    GFX_SetCursor(0, 1);
    GFX_PrintString("Quite$ a long string I @m.");
}

void Test_StringFrame(void)
{
    GFX_SetCursor(3, 2);
    GFX_PrintStringInFrame(18, 78, 4, 
        "Едут Гена с Чебурашкой на мотике."
        "Чебурашка на руле сидит"
        "Останавливает их гаишник."
        "И говорит: \"Уберите этого с руля\"."
    );
}

void Test_PartialClearing(void)
{
    Test_StringFrame();
    GFX_ClearRect(18, 2, 70, 4);
}

